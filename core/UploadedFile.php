        \UPLOAD_ERR_PARTIAL => 'The file "%s" was only partially uploaded.',
        \UPLOAD_ERR_NO_FILE => 'No file was uploaded.',
        \UPLOAD_ERR_CANT_WRITE => 'The file "%s" could not be written on disk.',
        \UPLOAD_ERR_NO_TMP_DIR => 'File could not be uploaded: missing temporary directory.',
        \UPLOAD_ERR_EXTENSION => 'File upload was stopped by a PHP extension.',
    ];

    public function __construct(string $name, string $mimeType, string $tmpName, int $error, int $size, string $path = null) {
        $this->name     = $name;
        $this->mimeType = $mimeType;
        $this->tmpName  = $tmpName;
        $this->error    = $error;
        $this->size     = $size;
        $this->path     = $path;
    }

    public function getName() :string {
        return $this->name;
    }

    public function getPath() :?string {
        return $this->path;
    }

    public function getMimeType() :string {
        return $this->mimeType;
    }

    public function getTmpName() :string {
        return $this->tmpName;
    }

    public function getError() :int {
        return $this->error;
    }

    public function getSize() :int {
        return $this->size;
    }

    public function getErrorMessage(): string {
        $errorCode = $this->error;
        $maxFilesize = \UPLOAD_ERR_INI_SIZE === $errorCode ? self::getMaxFilesize() / 1024 : 0;
        $message = self::UPLOADED_FILES_ERRORS[$errorCode] ?? 'The file "%s" was not uploaded due to an unknown error.';
        return sprintf($message, $this->name, $maxFilesize);
    }

    public function getContent(): string {
        $content = file_get_contents($this->tmpName);
        if (false === $content) {
            throw new Exception(sprintf('Could not get the content of the file "%s".', $this->tmpName));
        }
        return $content;
    }    

    public function moveTo(string $destination) {
        if ($this->error == 0) {
            return move_uploaded_file($this->tmpName, $destination);
        }
        return false;
    }

    private static function getMaxFilesize(): int|float {
        $sizePostMax = self::parseFilesize(\ini_get('post_max_size'));
        $sizeUploadMax = self::parseFilesize(\ini_get('upload_max_filesize'));
        return min($sizePostMax ?: \PHP_INT_MAX, $sizeUploadMax ?: \PHP_INT_MAX);
    }

    private static function parseFilesize(string $size): int|float {
        if ($size === '') {
            return 0;
        }

        $size = strtolower($size);
        $max = ltrim($size, '+');

        if (str_starts_with($max, '0x')) {
            $max = \intval($max, 16);
        } elseif (str_starts_with($max, '0')) {
            $max = \intval($max, 8);
        } else {
            $max = (int)$max;
        }

        switch (substr($size, -1)) {
            case 't':
            case 'g':
            case 'm':
            case 'k':
                $max *= 1024;
        }

        return $max;
    }

